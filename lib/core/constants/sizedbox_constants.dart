import 'package:flutter/material.dart';

class SizedBoxConstants {
  static SizedBox height(double height) => SizedBox(
        height: height,
      );
  static SizedBox width(double width) => SizedBox(
        width: width,
      );

  static SizedBox tenH() => const SizedBox(
        height: 10,
      );
  static SizedBox tenW() => const SizedBox(
        width: 10,
      );
  static SizedBox twentyH() => const SizedBox(
        height: 20,
      );
  static SizedBox twentyW() => const SizedBox(
        width: 20,
      );
  static SizedBox fortyH() => const SizedBox(
        height: 40,
      );
  static SizedBox fortyW() => const SizedBox(
        width: 40,
      );
  static SizedBox eightyH() => const SizedBox(
        height: 80,
      );
  static SizedBox eightyW() => const SizedBox(
        width: 80,
      );
}
